package com.company;

public class VariablesAndTypes {
    public static void main(String[] args) {

        int b = 3110;
        char c = 'w';
        byte d = 0;
        char e = 'r';
        short f = 1;
        char g = 'd';
        float h = 2.0f;
        boolean i = true;

        String output = "H" + b + " " + c + d + e + f + g + " " + h + " " + i;

        System.out.println(output);
    }
}
